# ── テスト実行 ────────────────────────────────────────────────
def run_tests():
    ocr = SBIPortfolioOCR()
    results = ocr._parse_v2(MOCK_BLOCKS, IMG_W, IMG_H)

    print(f"\n{'='*60}")
    print(f"  OCR v2 パーサーテスト")
    print(f"{'='*60}")
    print(f"  検出件数: {len(results)}件  /  期待: {len(EXPECTED)}件\n")

    ok = 0
    ng = 0

    for exp in EXPECTED:
        code = exp['code']
        got  = next((r for r in results if r['code'] == code), None)

        if got is None:
            print(f"  [NG] {code} {exp['name']:12s} → 検出されなかった")
            ng += 1
            continue

        errors = []
        if abs(got['current_price'] - exp['current_price']) > 1:
            errors.append(f"現在値: {got['current_price']} ≠ {exp['current_price']}")
        if abs(got['avg_cost'] - exp['avg_cost']) > 0.1:
            errors.append(f"平均単価: {got['avg_cost']} ≠ {exp['avg_cost']}")
        if abs(got['pnl_pct'] - exp['pnl_pct']) > 0.05:
            errors.append(f"損益率: {got['pnl_pct']:+.2f}% ≠ {exp['pnl_pct']:+.2f}%")
        if got['pnl_yen'] is not None and exp['pnl_pct'] != 0:
            sign_ok = (got['pnl_yen'] < 0) == (exp['pnl_pct'] < 0)
            if not sign_ok:
                errors.append(f"損益円の符号が逆: {got['pnl_yen']}")

        if errors:
            print(f"  [NG] {code} {exp['name']:12s}")
            for e in errors:
                print(f"       {e}")
            ng += 1
        else:
            sign = '+' if got['pnl_pct'] >= 0 else ''
            print(f"  [OK] {code} {exp['name']:12s}  "
                  f"現在値{got['current_price']:>8,.1f}  "
                  f"平均{got['avg_cost']:>9,.2f}  "
                  f"損益率{sign}{got['pnl_pct']:.2f}%  "
                  f"損益円{got['pnl_yen']:+,}" if got['pnl_yen'] else
                  f"  [OK] {code} {exp['name']:12s}  "
                  f"現在値{got['current_price']:>8,.1f}  "
                  f"平均{got['avg_cost']:>9,.2f}  "
                  f"損益率{sign}{got['pnl_pct']:.2f}%")
            ok += 1

    print(f"\n{'='*60}")
    print(f"  結果: {ok}件OK  {ng}件NG  (合格率 {ok/(ok+ng)*100:.0f}%)")
    print(f"{'='*60}\n")

    if ng == 0:
        print("  全テスト合格！OCR v2パーサーは正常に動作しています。")
    else:
        print("  NGがあります。パーサーを修正してください。")

    return ng == 0


# ── エッジケーステスト ────────────────────────────────────────
def run_edge_cases():
    """よくあるOCR誤読パターンをテスト"""
    ocr = SBIPortfolioOCR()
    print(f"\n{'='*60}")
    print(f"  エッジケーステスト")
    print(f"{'='*60}\n")

    # ケース1: 符号なし損益円（OCRが「-」を落とす）
    blocks_no_sign = [
        ("アゼアス",       bbox( 10, 50, 55, 17), 0.94),
        ("633円",          bbox(170, 50, 50, 17), 0.96),
        ("10677円",        bbox(262, 50, 88, 17), 0.70),  # ← 符号なし
        ("3901 建玉/特定", bbox( 10, 69,110, 15), 0.94),
        ("684.00円",       bbox(162, 69, 75, 15), 0.96),
        ("-7.78%",         bbox(274, 69, 66, 15), 0.97),
    ]
    r = ocr._parse_v2(blocks_no_sign, IMG_W, IMG_H)
    if r and r[0]['pnl_yen'] is not None and r[0]['pnl_yen'] < 0:
        print("  [OK] 符号なし損益円 → pnl_pctで符号補正 OK")
    else:
        pnl_yen = r[0]['pnl_yen'] if r else None
        print(f"  [NG] 符号なし損益円の補正失敗: pnl_yen={pnl_yen}")

    # ケース2: 「円」が認識されない
    blocks_no_yen = [
        ("セリア",         bbox( 10, 50, 45, 17), 0.96),
        ("3,440",          bbox(160, 50, 60, 17), 0.90),  # ← 円なし
        ("-11,845",        bbox(262, 50, 80, 17), 0.90),  # ← 円なし
        ("2782 建玉/特定", bbox( 10, 69,110, 15), 0.94),
        ("3,555.00",       bbox(152, 69, 80, 15), 0.90),  # ← 円なし
        ("-3.33%",         bbox(276, 69, 64, 15), 0.97),
    ]
    r = ocr._parse_v2(blocks_no_yen, IMG_W, IMG_H)
    if r and r[0]['current_price'] == 3440 and r[0]['avg_cost'] == 3555:
        print("  [OK] 「円」なし数値 → 正常にパース OK")
    else:
        print(f"  [NG] 「円」なし数値のパース失敗: {r}")

    # ケース3: コードと名前が別ブロック
    blocks_split = [
        ("東宝",           bbox( 10, 50, 35, 17), 0.97),
        ("1,461.5",        bbox(158, 50, 75, 17), 0.95),
        ("-462",           bbox(270, 50, 55, 17), 0.95),
        ("9602",           bbox( 10, 69, 38, 15), 0.94),   # ← コードのみ
        ("建玉/特定",      bbox( 52, 69, 62, 15), 0.93),   # ← 別ブロック
        ("1,465.00",       bbox(153, 69, 82, 15), 0.96),
        ("-0.32%",         bbox(275, 69, 65, 15), 0.97),
    ]
    r = ocr._parse_v2(blocks_split, IMG_W, IMG_H)
    if r and r[0]['code'] == '9602' and r[0]['current_price'] == 1461.5:
        print("  [OK] コードと建玉/特定が別ブロック → 正常 OK")
    else:
        print(f"  [NG] コード/建玉分割ケース失敗: {r}")

    # ケース4: カンマなし（OCRが「,」を落とす）
    blocks_no_comma = [
        ("INPEX",          bbox( 10, 50, 55, 17), 0.97),
        ("4123円",         bbox(160, 50, 65, 17), 0.88),  # ← カンマなし
        ("+6189円",        bbox(265, 50, 78, 17), 0.88),
        ("1605 建玉/特定", bbox( 10, 69,110, 15), 0.94),
        ("4058.00円",      bbox(155, 69, 80, 15), 0.88),
        ("+1.52%",         bbox(275, 69, 65, 15), 0.97),
    ]
    r = ocr._parse_v2(blocks_no_comma, IMG_W, IMG_H)
    if r and r[0]['current_price'] == 4123 and r[0]['avg_cost'] == 4058:
        print("  [OK] カンマなし数値 → 正常にパース OK")
    else:
        print(f"  [NG] カンマなし数値のパース失敗: {r}")

    print()


